public class loop {
    public static void main(String args[]){
        for(int i = 0; i<10; i++){
            System.out.println(i+1);
        }
        int j=1;
        while(j<=10){
            System.out.println(j*2);
            j++;
        }
        int k =1;
        do{
            System.out.println("k "+k);
            k++;
        }while(k<=10);

    }
    
}
